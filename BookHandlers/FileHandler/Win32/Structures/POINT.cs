using System.Runtime.InteropServices;

namespace BookHandlers.FileHandler.Win32.Structures
{
    [StructLayout(LayoutKind.Sequential)]
    internal struct POINT
    {
        public int X;
        public int Y;

        public POINT(int x, int y)
        {
            this.X = x;
            this.Y = y;
        }
    }
}
